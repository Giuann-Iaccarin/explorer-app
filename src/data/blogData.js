// Funzioni utility per il blog
export const getBlogStats = () => {
    return {
        totalPosts: blogPostsData.length,
        totalCities: [...new Set(blogPostsData.map(post => post.city))].length,
        totalLikes: blogPostsData.reduce((sum, post) => sum + post.likes, 0),
        totalComments: blogPostsData.reduce((sum, post) => sum + post.comments, 0)
    };
};

export const getCities = () => {
    return [...new Set(blogPostsData.map(post => post.city))];
};

export const filterPosts = (posts, { searchTerm, city, type }) => {
    return posts.filter(post => {
        const matchesSearch = !searchTerm ||
            post.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
            post.description.toLowerCase().includes(searchTerm.toLowerCase());

        const matchesCity = city === 'all' || post.city === city;
        const matchesType = type === 'all' || post.missionType === type;

        return matchesSearch && matchesCity && matchesType;
    });
};

export const sortPosts = (posts, sortBy = 'date') => {
    const sorted = [...posts];

    switch (sortBy) {
        case 'date':
            return sorted.sort((a, b) => new Date(b.date) - new Date(a.date));
        case 'likes':
            return sorted.sort((a, b) => b.likes - a.likes);
        case 'comments':
            return sorted.sort((a, b) => b.comments - a.comments);
        default:
            return sorted;
    }
};